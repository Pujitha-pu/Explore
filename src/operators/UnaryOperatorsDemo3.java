package operators;

public class UnaryOperatorsDemo3 {

	public static void main(String[] args) {
		
		int x=10;
		
			
		int y=20;
		
		
		System.out.println(++x+y--);   //11+20=31 // x=11 y=19 
		
		System.out.println(++x+y--);  //12+19=31     // 
		
		System.out.println(x); // 12
		System.out.println(y); // 18

	}

}
